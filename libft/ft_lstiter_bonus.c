#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	while (lst)
	{
		if (lst->content)
			(*f)(lst->content);
		if (lst->next)
			lst = lst->next;
		else
			break ;
	}
}
